#This program calculates the miles per gallon of a vehicle.

tank_size = 15 # How many gallons that were used to get to the distance of 600.
distance = 600 # How many miles it took to use 15 gallons of gas
mpg = distance / tank_size # This is a formula to find out the miles per gallon, which is equal to the distance divided by the tank size.
print("Miles per gallon:", mpg)